async function writePresenceRoster(projectRoot: string, agents: PresenceEntry[]): Promise<void> {
  const roster: PresenceRoster = {
    version: 1,
    agents
  };
  await writeTextFile(join(projectRoot, ".codex-agents/presence.json"), JSON.stringify(roster, null, 2) + "\n");
}

async function createDemoWorkspace(): Promise<string> {
  const root = await mkdtemp(join(tmpdir(), "codex-agents-office-preview-"));
  const templates = demoTemplateFiles();
  for (const [relativePath, content] of Object.entries(templates)) {
    await writeTextFile(join(root, relativePath), content);
  }
  return root;
}

async function applyStage(projectRoot: string, stage: DemoStage): Promise<void> {
  if (stage.files) {
    for (const [relativePath, content] of Object.entries(stage.files)) {
      await writeTextFile(join(projectRoot, relativePath), content);
    }
  }
  await writePresenceRoster(projectRoot, stage.agents);
}

export async function runDemoPreview(args: string[]): Promise<void> {
  const options = parseDemoPreviewArgs(args);
  const projectRoot = await createDemoWorkspace();
  const stages = buildDemoStages(projectRoot);
  const timers = new Set<NodeJS.Timeout>();
  let cleaned = false;

  const cleanup = () => {
    if (cleaned) {
      return;
    }
    cleaned = true;
    for (const timer of timers) {
      clearTimeout(timer);
    }
    timers.clear();
    if (!options.keep && existsSync(projectRoot)) {
      rmSyncImmediate(projectRoot, { recursive: true, force: true });
    }
  };

  process.once("SIGINT", cleanup);
  process.once("SIGTERM", cleanup);
  process.once("exit", cleanup);

  await applyStage(projectRoot, stages[0]);
  for (const stage of stages.slice(1)) {
    const timer = setTimeout(() => {
      void applyStage(projectRoot, stage);
      timers.delete(timer);
    }, stage.afterMs);
    timers.add(timer);
  }

  console.log(`Preview workspace: ${projectRoot}`);
  console.log(`Preview URL: http://${options.host}:${options.port}/?project=${encodeURIComponent(projectRoot)}`);
  console.log("This run creates a disposable fake app with demo agents, demo skills, and a scripted office workflow.");
  console.log(options.keep ? "Workspace cleanup is disabled for this run (--keep)." : "Workspace will be removed when the preview stops.");
  if (options.durationMs > 0) {
    console.log(`Preview duration: ${Math.round(options.durationMs / 1000)}s`);
  } else {
    console.log("Preview duration: until interrupted");
  }

  await startWebServer([projectRoot, "--port", String(options.port), "--host", options.host]);

  if (options.durationMs > 0) {
    const timer = setTimeout(async () => {
      timers.delete(timer);
      cleanup();
      process.exit(0);
    }, options.durationMs);
    timers.add(timer);
  }

  await new Promise<void>(() => {});
}

export async function deletePreviewWorkspace(projectRoot: string): Promise<void> {
  await rm(resolve(projectRoot), { recursive: true, force: true });
}
