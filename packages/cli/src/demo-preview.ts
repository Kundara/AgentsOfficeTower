interface DemoPreviewOptions {
  host: string;
  port: number;
  keep: boolean;
  durationMs: number;
}

interface DemoStage {
  afterMs: number;
  agents: PresenceEntry[];
  files?: Record<string, string>;
}

function parseDemoPreviewArgs(args: string[]): DemoPreviewOptions {
  let port = 4181;
  let host = "127.0.0.1";
  let keep = false;
  let durationMs = 75_000;

  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index];
    if (arg === "--port" && args[index + 1]) {
      port = Number.parseInt(args[index + 1], 10);
      index += 1;
      continue;
    }
    if (arg === "--host" && args[index + 1]) {
      host = args[index + 1];
      index += 1;
      continue;
    }
    if (arg === "--duration" && args[index + 1]) {
      const seconds = Number.parseFloat(args[index + 1]);
      durationMs = Number.isFinite(seconds) && seconds > 0 ? Math.round(seconds * 1000) : 0;
      index += 1;
      continue;
    }
    if (arg === "--keep") {
      keep = true;
    }
  }

  return {
    host,
    port: Number.isFinite(port) ? port : 4181,
    keep,
    durationMs
  };
}

async function writeTextFile(filePath: string, content: string): Promise<void> {
  await mkdir(dirname(filePath), { recursive: true });
  await writeFile(filePath, content, "utf8");
}

function isoAt(baseMs: number, offsetMs: number): string {
  return new Date(baseMs + offsetMs).toISOString();
}

function demoPresence(
  baseMs: number,
  offsetMs: number,
  input: Omit<PresenceEntry, "updatedAt">
): PresenceEntry {
  return {
    ...input,
    updatedAt: isoAt(baseMs, offsetMs)
  };
}

